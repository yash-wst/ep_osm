-module(ep_osm_cap_fields).
-compile(export_all).
-include("records.hrl").


%------------------------------------------------------------------------------
% fields
%------------------------------------------------------------------------------
f(code = I) ->
	itf:textbox(?F(I, "CAP Centre Code"));

f(name = I) ->
	itf:textbox(?F(I, "CAP Centre Name"));

f(ips = I) ->
	F = mylist:get(I, "IPs", list),
	F#field {
		validators=[]
	};

f(osm_cap_fk = I) ->
	F = itf:textbox_picker(?F(I, "CAP Centre")),
	F#field {
		options=#search {
			title=?LN("Select CAP Centre"),
			db=ep_osm_cap:db(),
			displayfs=ep_osm_cap:fs(search),
			filterfs=ep_osm_cap:fs(search),
			size=10
		}
	};


f(O) -> throw(O).

%------------------------------------------------------------------------------
% validators
%------------------------------------------------------------------------------
validator(O) ->
	throw(O).

%------------------------------------------------------------------------------
% options
%------------------------------------------------------------------------------
options(gender) ->
	itf:options([
		?F(male),
		?F(female),
		?F(other)
	]).

