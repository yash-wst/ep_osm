%------------------------------------------------------------------------------
% save
%------------------------------------------------------------------------------

%
% override before create function
%
before_create(FsToSave) ->

	%
	% init
	%
	Code = itf:val2(FsToSave, code),



	%
	% assert - code does not exist already
	%
	Fs = [
		itf:build(?OSMCAP(code), Code)
	],
	#db2_find_response {docs=Docs} = db2_find:get_by_fs(
		ep_osm_cap_api:db(), Fs
	),
	?ASSERT(
		Docs == [],
		{Code, "code already exists"}
	),



	%
	% all ok
	%
	FsToSave.


%
% override before save function
%
