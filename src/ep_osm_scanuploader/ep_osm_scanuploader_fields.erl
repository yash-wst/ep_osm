-module(ep_osm_scanuploader_fields).
-compile(export_all).
-include("records.hrl").


%------------------------------------------------------------------------------
% fields
%------------------------------------------------------------------------------
f(name = I) ->
	itf:textbox(?F(I));
f(gender = I) ->
	itf:dropdown(?F(I), options(I));
f(O) -> throw(O).

%------------------------------------------------------------------------------
% validators
%------------------------------------------------------------------------------
validator(O) ->
	throw(O).

%------------------------------------------------------------------------------
% options
%------------------------------------------------------------------------------
options(gender) ->
	itf:options([
		?F(male),
		?F(female),
		?F(other)
	]).

