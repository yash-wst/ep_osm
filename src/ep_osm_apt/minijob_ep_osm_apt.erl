
-module(minijob_ep_osm_apt).
-compile(export_all).
-include("records.hrl").
-include_lib("nitrogen_core/include/wf.hrl").


%------------------------------------------------------------------------------
% main
%------------------------------------------------------------------------------

main() ->
	ita:auth(itx, ?MODULE, ?AKIT(#template {file="lib/itx/priv/static/templates/html/entered.html"})).

title() ->
	?LN("minijob_ep_osm_apt").

heading() ->
	title().


%------------------------------------------------------------------------------
% access
%------------------------------------------------------------------------------
access(_, ?ADMIN) -> true;
access(_, _) -> false.





%------------------------------------------------------------------------------
% fields
%------------------------------------------------------------------------------



%
% fs - doc
%
fs(Doc) ->

	%
	% get dig module
	%
	DigModule = ?L2A(itf:val(Doc, dig_module)),


	%
	% return fields
	%
	DigModule:fs(search) ++ fs().


%
% fs -
%
fs() -> [
	itf:build(itf:textbox(?F(dig_module, "Dig Module")), "dig_mm_ep_osm_apt"),
	itf:build(itf:textbox(?F(send_count, "Send Count")), minijobcontext:q(send_count))
].


%------------------------------------------------------------------------------
% concurrency
%------------------------------------------------------------------------------

concurrency() ->
	one_per_module.



%------------------------------------------------------------------------------
% size
%------------------------------------------------------------------------------

size(_Doc) ->
	100.



%------------------------------------------------------------------------------
% progress
%------------------------------------------------------------------------------
progress(Doc) ->
	progress(Doc, 1).
progress(Doc, Increment) ->
	minijob_api:update_progress(Doc, Increment).



%------------------------------------------------------------------------------
% layouts
%------------------------------------------------------------------------------

layout() ->
	minijob:layout(?MODULE).



%------------------------------------------------------------------------------
% events
%------------------------------------------------------------------------------
event(E) ->
	minijob:event(?MODULE, E).



%------------------------------------------------------------------------------
% create and run
%------------------------------------------------------------------------------

create_and_run(Fs) ->
	minijob:create_and_run(?MODULE, itf:fs_merge(Fs, fs())).



%------------------------------------------------------------------------------
% do
%------------------------------------------------------------------------------

do(Doc) ->

	%
	% init
	%
	DigModule = ?L2A(itf:val(Doc, dig_module)),
	Fs = itf:d2f(Doc, DigModule:fs(search)),
	SendCount = ?S2I(itf:val(Doc, send_count)),
	dig_mm_ep_osm_apt:handle_send_confirmed(Fs, SendCount, 0).


%------------------------------------------------------------------------------
% commit
%------------------------------------------------------------------------------

commit() ->
	ok.


%------------------------------------------------------------------------------
% misc
%------------------------------------------------------------------------------



%------------------------------------------------------------------------------
% end
%------------------------------------------------------------------------------

