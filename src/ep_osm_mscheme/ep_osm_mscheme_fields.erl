-module(ep_osm_mscheme_fields).
-compile(export_all).
-include("records.hrl").


%------------------------------------------------------------------------------
% fields
%------------------------------------------------------------------------------
f(name = I) ->
	itf:textbox(?F(I, "Name"));


f(state = I) ->
	itf:dropdown(?F(I), options(I));


f(scheme = I) ->
	itf:textbox(?F(I, "Marking Scheme"));


f(O) -> throw(O).

%------------------------------------------------------------------------------
% validators
%------------------------------------------------------------------------------
validator(O) ->
	throw(O).

%------------------------------------------------------------------------------
% options
%------------------------------------------------------------------------------

options(state) ->
	itf:options([
		?F(draft, "Draft"),
		?F(published, "Published"),
		?F(discarded, "Discarded")
	]).

