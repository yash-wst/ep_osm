-module(ep_osm_bundle_fields).
-compile(export_all).
-include("records.hrl").


%------------------------------------------------------------------------------
% fields
%------------------------------------------------------------------------------

f(osm_exam_fk = I) ->
	F = itf:textbox_picker(?F(I, "OSM Exam")),
	F#field {
		options=#search {
			title=?LN("Select OSM Exam"),
			db=anptests:getdb(),
			displayfs=anptest:fs(search),
			filterfs=anptest:fs(search),
			size=10
		}
	};

f(number = I) ->
	itf:textbox_int(?F(I, "Bundle Number"));

f(createdby = I) ->
	itf:createdby(?F(I, "Created By"));

f(createdon = I) ->
	itf:createdon(?F(I, "Created On"));



f(O) -> throw(O).

%------------------------------------------------------------------------------
% validators
%------------------------------------------------------------------------------
validator(O) ->
	throw(O).

%------------------------------------------------------------------------------
% options
%------------------------------------------------------------------------------
options(gender) ->
	itf:options([
		?F(male),
		?F(female),
		?F(other)
	]).

